    private boolean kiemtratuanhoc(String a)
    {
        String []chuoi;
        chuoi= a.split(",");
        int len=a.length();
        if(len==1)
        {
                try{
                    if(Integer.parseInt(chuoi[0])>8) return false;
                    return true;
                
                }
                catch(Exception ex)
                {
                    return false;
                }
        }
        else
        {
            for(int i=0;i<(len/2)+1;i++)
            {
                if(chuoi[i].equals("")) break;
                try{
                    Integer.parseInt(chuoi[i]);
                }
                catch(Exception ex)
                {
                    return false;
                }
            }
        }    
        return true;
    }
