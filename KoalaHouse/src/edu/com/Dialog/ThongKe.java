    private void setSelectComboBox(JComboBox x, Object obj) {
        for (int i = 0; i < x.getItemCount(); i++) {
            if (x.getItemAt(i).toString().equalsIgnoreCase(obj.toString())) {
                x.setSelectedIndex(i);
                break;
            }
        }
    }
